package com.example.gp2project;

public class GroupModel {

    String Name;
    String UserAdminKey;
    String key;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getUserAdminKey() {
        return UserAdminKey;
    }

    public void setUserAdminKey(String userAdminKey) {
        UserAdminKey = userAdminKey;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
